// src/lib/supabase/server.ts
// Server-side Supabase client for Server Components and Server Actions
// Uses createServerClient from @supabase/ssr with Next.js cookies
// RELEVANT FILES: src/lib/supabase/client.ts, src/middleware.ts, src/actions/players.ts

import { createServerClient } from '@supabase/ssr';
import { cookies } from 'next/headers';

export async function createClient() {
  const cookieStore = await cookies();

  return createServerClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!,
    {
      cookies: {
        getAll() {
          return cookieStore.getAll();
        },
        setAll(cookiesToSet) {
          try {
            cookiesToSet.forEach(({ name, value, options }) =>
              cookieStore.set(name, value, options)
            );
          } catch {
            // setAll is called from a Server Component — ignore.
            // Middleware will handle session refresh.
          }
        },
      },
    }
  );
}

